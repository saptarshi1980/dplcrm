
package in.net.dpl.model;

public class TenderModel {

	String tenderGroup=null;
	String tenderRefNo=null;
	String tenderType=null;
	String estimatedValue=null;
	String openingDateTime=null;
	String closingDateTime=null;
	String prebidDateTime=null;
	String submissionDateTime=null;
	String scope=null;
	String fileName=null;
	String count=null;
	
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getTenderGroup() {
		return tenderGroup;
	}
	public void setTenderGroup(String tenderGroup) {
		this.tenderGroup = tenderGroup;
	}
	public String getTenderRefNo() {
		return tenderRefNo;
	}
	public void setTenderRefNo(String tenderRefNo) {
		this.tenderRefNo = tenderRefNo;
	}
	public String getTenderType() {
		return tenderType;
	}
	public void setTenderType(String tenderType) {
		this.tenderType = tenderType;
	}
	public String getEstimatedValue() {
		return estimatedValue;
	}
	public void setEstimatedValue(String estimatedValue) {
		this.estimatedValue = estimatedValue;
	}
	public String getOpeningDateTime() {
		return openingDateTime;
	}
	public void setOpeningDateTime(String openingDateTime) {
		this.openingDateTime = openingDateTime;
	}
	public String getClosingDateTime() {
		return closingDateTime;
	}
	public void setClosingDateTime(String closingDateTime) {
		this.closingDateTime = closingDateTime;
	}
	public String getPrebidDateTime() {
		return prebidDateTime;
	}
	public void setPrebidDateTime(String prebidDateTime) {
		this.prebidDateTime = prebidDateTime;
	}
	public String getSubmissionDateTime() {
		return submissionDateTime;
	}
	public void setSubmissionDateTime(String submissionDateTime) {
		this.submissionDateTime = submissionDateTime;
	}
	public String getScope() {
		return scope;
	}
	public void setScope(String scope) {
		this.scope = scope;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	
	
}
